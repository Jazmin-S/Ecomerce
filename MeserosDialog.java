    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        this.mesero.setNombre(this.txt_nombre.getText());
        this.mesero.setApellidos(this.txt_apellidos.getText());
        if(this.mesero.getNombre() == null || this.mesero.getNombre().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, 
                        "Debes seleccionar ingresar el nombre de la mesero",
                        "Advertencia", 
                        JOptionPane.WARNING_MESSAGE);
            return;
        }
        boolean ok = false;
        if(this.nuevoregistro){
            ok = MeseroDAO.registrar(mesero);
        }else{
            ok = MeseroDAO.actualizar(mesero);
        }
        if(ok){
            JOptionPane.showMessageDialog(this, 
                        "Mesero guardado correctamente",
                        "Informaci√≥n", 
                        JOptionPane.INFORMATION_MESSAGE);
        }else{
            JOptionPane.showMessageDialog(this, 
                        "No se puede guardar el mesero",
                        "Advertencia", 
                        JOptionPane.WARNING_MESSAGE);
        }
        this.dispose();
    }//GEN-LAST:event_btn_guardarActionPerformed

