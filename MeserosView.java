    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed
        String nombre = this.txt_filtro_nombre.getText();
        if(nombre==null || nombre.trim().isEmpty()){
            this.meseros = MeseroDAO.getMeseros();
        }else{
            this.meseros = MeseroDAO.buscarPorNombre(nombre.trim());
        }
        cargarMeserosEnTabla();
        if(this.meseros.size() == 0){
            JOptionPane.showMessageDialog(this, "No se encontraron registros",
                                                "Información", 
                                                JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btn_buscarActionPerformed

