    private void cargarMeserosEnTabla(){
        String [] columnas = {"CLAVE","NOMBRE","APELLIDOS"};
        DefaultTableModel model = new DefaultTableModel();
        model = new DefaultTableModel(new Object[0][0], columnas){
            @Override
            public boolean isCellEditable(int row, int column) {
               return false;
            }
        };
        if(meseros!=null){
            for(Mesero m : meseros){
                Object fila[] = new Object[4];
                fila[0] = m.getIdMesero();
                fila[1] = m.getNombre();
                fila[2] = m.getApellidos();
                model.addRow(fila);
            }
        }
        this.tbl_datos.setModel(model);
    }
    
