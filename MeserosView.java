    private void btn_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editarActionPerformed
        int indexselected = this.tbl_datos.getSelectedRow();
        if(indexselected < 0){
            JOptionPane.showMessageDialog(this, 
                        "Debes seleccionar el registro que deseas eliminar",
                        "Advertencia", 
                        JOptionPane.WARNING_MESSAGE);
            return;
        }
        Mesero mesero = this.meseros.get(indexselected); 
        MeserosDialog dialogo = new MeserosDialog(this, true, mesero);
        this.meseros = MeseroDAO.getMeseros();
        cargarMeserosEnTabla();
    }//GEN-LAST:event_btn_editarActionPerformed

    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        int indexselected = this.tbl_datos.getSelectedRow();
        if(indexselected < 0){
            JOptionPane.showMessageDialog(this, 
                        "Debes seleccionar el registro que deseas eliminar",
                        "Advertencia", 
                        JOptionPane.WARNING_MESSAGE);
            return;
        }
        Integer idmesero = this.meseros.get(indexselected).getIdMesero();
        int respuesta = JOptionPane.showConfirmDialog(this,
                            "¿Borrar mesero seleccionado?",
                            "Borrar mesero",
                            JOptionPane.YES_NO_OPTION);
        if (respuesta == JOptionPane.YES_OPTION) {
            if(MeseroDAO.eliminar(idmesero)){
                JOptionPane.showMessageDialog(this, 
                        "Mesero eliminada correctamente",
                        "Confirmación", 
                        JOptionPane.INFORMATION_MESSAGE);
                this.meseros = MeseroDAO.getMeseros();
                cargarMeserosEnTabla();
            }else{
                JOptionPane.showMessageDialog(this, 
                        "No se puede eliminar el mesero seleccionad",
                        "Advertencia", 
                        JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_btn_eliminarActionPerformed

